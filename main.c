void driver_tranfer(hand_t *hand){
	const int closed = 750;
	uint16_t thumb_curl, index_curl, middle_curl, ring_curl, pinky_curl;
	thumb_curl = finger_get_curl(&hand->thumb);
	index_curl = finger_get_curl(&hand->index);
	middle_curl = finger_get_curl(&hand->middle);
	ring_curl = finger_get_curl(&hand->ring);
	pinky_curl = finger_get_curl(&hand->pinky);
	int is_index = index_curl > closed;
	int is_middle = middle_curl > closed;
	int is_ring = ring_curl > closed;
	int is_pinky = pinky_curl > closed;
	int finger_sum = is_index + is_middle + is_ring + is_pinky;

	int is_pinch = (finger_sum == 1);
	int is_grab = (finger_sum >= 3);

	printf("A%dB%dC%dD%dE%d",
			thumb_curl,
			index_curl,
			middle_curl,
			ring_curl,
			pinky_curl);

	if(is_grab){
		printf("L");
	}
	else if(is_pinch){
//		printf("M");

		if(is_index){
			printf("IP%d", 2*(index_curl - 500));
		}

	}

	printf("\n");
}

static inline void poll_uart(hand_t *hand){
	uint8_t rx_buff[1024] = {0};
	HAL_UART_Receive(&huart2, rx_buff, 1024, 10);
	//	  HAL_UART_Receive_IT(&huart2, rx_buff, 1024);

	int i = 0;
	int percent;
	int buzz, buzzdec;
	while(rx_buff[i]){
	  percent = 0;
//	  printf("str: %s\n", rx_buff);
	  switch (rx_buff[i]) {
		case 'A':
			sscanf((char *)(rx_buff + i), "A%d", &percent);
			finger_set_servo_inverted(&hand->thumb, percent);
			break;
		case 'B':
			sscanf((char *)(rx_buff + i), "B%d", &percent);
			finger_set_servo_inverted(&hand->index, percent);
			break;
		case 'C':
			sscanf((char *)(rx_buff + i), "C%d", &percent);
			finger_set_servo_inverted(&hand->middle, percent);
			break;
		case 'D':
			sscanf((char *)(rx_buff + i), "D%d", &percent);
			finger_set_servo_inverted(&hand->ring, percent);
			break;
		case 'E':
			sscanf((char *)(rx_buff + i), "E%d", &percent);
			finger_set_servo_inverted(&hand->pinky, percent);
			break;
		case 'G':
			sscanf((char *)(rx_buff + i), "G%d.%d", &buzz, &buzzdec);
			hand_set_buzz(hand, buzz, buzzdec);
			break;
		default:
			return;
			break;
	}

		if(percent/1000){
			i += 5;
		}else if(percent/100){
			i += 4;
		}else if(percent/10){
			i += 3;
		}else{
			i += 2;
		}
		if(rx_buff[i] == '\n'){
			  break;
		}
	}
	memset(rx_buff, 0, 1024);
}

